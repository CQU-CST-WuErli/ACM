vector<ll> vc;
ll m;
map<ll, pair<int, ll> > mp;

void init() {
	for (ll i = 1; i <= 100010; i++) vc.push_back(i * i * i);
}

pair<int, ll> gao(ll m) {
	if (m < 8LL) return make_pair(m, m);
	if (mp.find(m) != mp.end()) return mp[m];
	int p = upper_bound(vc.begin(), vc.end(), m) - vc.begin();
	p--;
	pair<int, ll> tmp1 = gao(vc[p] - 1);
	pair<int, ll> tmp2 = gao(m - vc[p]);
	tmp2.first += 1; tmp2.second += vc[p];
	pair<int, ll> ans = max(tmp1, tmp2);
	return mp[m] = ans;
}

