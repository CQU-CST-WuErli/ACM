int n;
vector<int> g[100010];
int sz[100010];
double ans[100010];

void dfs1(int u) {
	sz[u] = 1;
	for (auto& v : g[u]) {
		dfs1(v);
		sz[u] += sz[v];
	}
}

void dfs2(int u) {
	for (auto& v : g[u]) {
		ans[v] = ans[u] + 1 + (double)(sz[u] - sz[v] - 1) / 2.0;
		dfs2(v);
	}
}

