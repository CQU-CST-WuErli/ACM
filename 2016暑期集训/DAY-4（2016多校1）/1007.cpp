void solve(int n,int m){
	ll dp[11][1<<11];MEM(dp,0);
	dp[0][0]=1;
	for(int i=1;i<=n;i++){
		for(int j=0;j<(1<<(m));j++){
			for(int k=1;k<(1<<(m));k++){
				int cnt = 0;
				for(int s=0;s<m;s++){
					if((1<<s) & k)cnt++;
				}
				if(cnt == 0)continue;
				dp[i][j|k] = (dp[i][j|k]+dp[i-1][j]%MOD*(ll)pow(2,cnt)%MOD)%MOD;
			}
		}
	}
	cout << dp[n][(1<<m)-1] << endl;
}
